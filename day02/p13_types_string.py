#  单行字符串
empty_str = ''
print(empty_str)

space_str = ' '
print(space_str)

s1 = 'Hello, 中国'
print(s1)

s2 = "Hello, 中国"
print(s2)

s3 = '''Hello, 中国'''
print(s3)

s4 = """Hello, 中国"""
print(s4)

#  多行字符串
s5 = '''第一行
第二行
第三行'''
print(s5)

s6 = """第一行
第二行
第三行"""
print(s6)

